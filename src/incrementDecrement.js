//++ and --

//post increment (later/after increment)

let x=5;
console.log(x++);  //output:5
console.log(x);    //output:6


let total=100;
console.log(total++);  //output:100
console.log(total);    //output:101

let x=-99;
console.log(x++);  //output:-99
console.log(x);    //output:-98

let y=x++;
console.log(y);    //output:-98
console.log(x);    //output:-97





//pre increment means increments first and then print later

let p=5;
let q=++p;


console.log(p);   //output:6
console.log(q);     //output:6


let p1=-100;
let p2=++p1;

console.log(p1);   //output:-99
console.log(p2);   //output:-99


let u=0;
let v=++u;
console.log(u);  //output:1
console.log(v);  //output:1



//post (later /after) decrement

let l=100;
console.log(l--);  //output:100
console.log(l);    //output:99

let m=-100;
console.log(m--);  //output:-100
console.log(m);    //output:-101


//pre decrement (first decrement and then print later)

let a=100;
let b=--a;
console.log(a);   //output:99
console.log(b);   //output:99










